public class IfElse2 {

	public static void main(String[] args) {
		
		int x = 10; int y = 11;
		  
		  if(!(x < y) || (x == y))  // !(10<11) or (10 == 11)    ! = Not  so !(10<11) means is false
		  { 
			  System.out.println("Condition is TRUE");
		  }
		  else
		  { 
			  System.out.println("Condition is FALSE"); 
		  }

	}

}
